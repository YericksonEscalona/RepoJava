    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {
            if(txtNombre.getText() != null && txtNombre.getText().length()>0 && 
            txtApellido.getText() != null && txtApellido.getText().length()>0 &&
            txtSexo.getText() != null && txtSexo.getText().length()>0 &&
            txtCedula.getText() != null && txtCedula.getText().length()>0 &&
            cboCivil.getSelectedIndex()!=0 &&
            cboEstado.getSelectedIndex()!=0       
            ) {
                
                Random r1 = new Random();
                int n = (int)(r1.nextDouble()*5000);
                doc = new Documento();
                doc.setId_documento(n);//codigo del registro de documento
                doc.setHayCar(cbCarta.isSelected());
                doc.setHayCed(cbCedula.isSelected());
                doc.setHayFon(cbFondo.isSelected());
                doc.setHayNot(cbNotas.isSelected());
                doc.setHayTit(cbTitulo.isSelected());
                
                estudiante = new Estudiante();
                estudiante.setNombre(txtNombre.getText());
                estudiante.setApellido(txtApellido.getText());
                estudiante.setSexo(txtSexo.getText());
                estudiante.setCedula(txtCedula.getText());
                estudiante.setIdEstado(cboEstado.getSelectedIndex());
                estudiante.setIdDoc(n);
                estudiante.setIdEdoCivil(cboCivil.getSelectedIndex());

                int confirmado = JOptionPane.showConfirmDialog(this, "¿Estas seguro?");
                if (JOptionPane.OK_OPTION == confirmado){
                    DocServ.guardar(cx.conectar(), doc);//documento guardado
                    estudianteServicio.guardar(cx.conectar(), estudiante);//guardado en la bd
                    JOptionPane.showMessageDialog(this, "¡Registro guardado exitosamente!");
                    limpio();
                }
            }else{
                JOptionPane.showMessageDialog(this, "¡Error, Complete todos los datos!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        if(txtBuscar.getText()!= null && txtBuscar.getText().length()>0){
            int id = Integer.parseInt(txtBuscar.getText());
            try {
                estudiante = estudianteServicio.recuperarPorId(cx.conectar(), id);
                btnModificar.setEnabled(true);
                btnEliminar.setEnabled(true);
                btnGuardar.setEnabled(false);
                txtNombre.setText(estudiante.getNombre());
                txtApellido.setText(estudiante.getApellido());
                txtCedula.setText(estudiante.getCedula());
                txtSexo.setText(estudiante.getSexo());
                cboCivil.setSelectedIndex(estudiante.getIdEdoCivil());
                cboEstado.setSelectedIndex(estudiante.getIdEstado());
                doc = DocServ.recuperarPorId(cx.conectar(), estudiante.getIdDoc());
                cbCarta.setSelected(doc.isHayCar());
                cbCedula.setSelected(doc.isHayCed());
                cbFondo.setSelected(doc.isHayFon());
                cbTitulo.setSelected(doc.isHayTit());
                cbNotas.setSelected(doc.isHayNot());
                
            } catch (SQLException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "¡Error, ingrese un ID a buscar!");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

