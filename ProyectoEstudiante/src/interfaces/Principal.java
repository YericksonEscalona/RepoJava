    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        int confirmado = JOptionPane.showConfirmDialog(this, "¿Estas seguro?");
        if (JOptionPane.OK_OPTION == confirmado){
            try {
                estudiante.setNombre(txtNombre.getText());
                estudiante.setApellido(txtApellido.getText());
                estudiante.setSexo(txtSexo.getText());
                estudiante.setCedula(txtCedula.getText());
                estudiante.setIdEstado(cboEstado.getSelectedIndex());
                //estudiante.setIdDoc(3129);
                estudiante.setIdEdoCivil(cboCivil.getSelectedIndex());
                
                estudianteServicio.guardar(cx.conectar(), estudiante);//modificar en la bd
                doc.setId_documento(estudiante.getIdDoc());
                doc.setHayCar(cbCarta.isSelected());
                doc.setHayCed(cbCedula.isSelected());
                doc.setHayFon(cbFondo.isSelected());
                doc.setHayNot(cbNotas.isSelected());
                doc.setHayTit(cbTitulo.isSelected());
                DocServ.modificar(cx.conectar(), doc);//documento guardado
                
                JOptionPane.showMessageDialog(this, "¡Registro modificado exitosamente!");
                limpio();
            } catch (SQLException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

